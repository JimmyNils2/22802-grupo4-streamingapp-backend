const mongoose = require('mongoose');

const user = mongoose.Schema({
  name: {
    type: String,
    required: true,
    trim: true
  },
  lastname: {
    type: String,
    required: true,
    trim: true
  },
  email: {
    type: String,
    required: true,
    trim: true
  },
  password: {
    type: String,
    required: true,
    trim: true
  }
},
  {
    timestamps: true
  });

module.exports = mongoose.model('User', user);